// Helper functions for strain operations
export const getStrainByName = (name) => {
  const normalizedName = name.toLowerCase().trim();
  return strainDatabase.find(
    strain => strain.name.toLowerCase() === normalizedName
  );
};

export const searchStrains = (query) => {
  const normalizedQuery = query.toLowerCase().trim();
  return strainDatabase.filter(
    strain => strain.name.toLowerCase().includes(normalizedQuery)
  );
};

export const getStrainsByType = (type) => {
  return strainDatabase.filter(strain => strain.type === type);
};

export const getAllStrainNames = () => {
  return strainDatabase.map(strain => strain.name);
};

export const getStrainType = (name) => {
  const strain = getStrainByName(name);
  return strain ? strain.type : null;
};

